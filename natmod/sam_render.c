/*
 * SAM Renderer - native MicroPython C module for RP2040.
 *
 * Implements the hot render loop (process_frames + timetable output)
 * as compiled C for ~50-100x speedup over Python bytecode.
 *
 * Python API:
 *   import sam_render
 *   output_buf = sam_render.process_frames(
 *       freq1, freq2, freq3, amp1, amp2, amp3,
 *       pitches, samp_flags, num_frames, speed
 *   )
 *   # Returns bytearray of 8-bit unsigned PCM at 22050 Hz (via timetable)
 *   # Downsampled 3:1 to 7350 Hz for PWM playback
 */

#include "py/dynruntime.h"

